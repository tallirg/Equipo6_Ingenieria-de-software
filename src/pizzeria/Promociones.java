    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = txtPromoNom.getText().trim();
    String descripcion = txtDescripcion.getText().trim();
    String precioTexto = txtPrecio.getText().trim();

    if (nombre.isEmpty() || descripcion.isEmpty() || precioTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, rellene todos los campos.");
        return;
    }

    BigDecimal precio;
    try {
        precio = new BigDecimal(precioTexto);
        if (precio.compareTo(BigDecimal.ZERO) <= 0) {
            JOptionPane.showMessageDialog(this, "El precio debe ser mayor que 0.");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingrese un precio v치lido.");
        return;
    }

    EntityManagerFactory emf = Persistence.createEntityManagerFactory("PizzeriaPU");
    EntityManager em = emf.createEntityManager();

    try {
        // VERIFICAR DUPLICADO
        List<Producto> repetido = em.createQuery(
                "SELECT p FROM Producto p WHERE LOWER(p.nombre) = :n", Producto.class)
                .setParameter("n", nombre.toLowerCase())
                .getResultList();

        if (!repetido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Esa promoci칩n ya existe.");
            em.close();
            emf.close();
            return;
        }
    // Crear producto
        Producto nuevo = new Producto();
        nuevo.setNombre(nombre);
        nuevo.setDescripcion(descripcion);
        nuevo.setPrecio(precio);
        nuevo.setIdCategoria(new Categoria(5));

        ProductoJpaController control = new ProductoJpaController(emf);
        control.create(nuevo);

        // 游댠 Crear inventario autom치ticamente
        Inventario inv = new Inventario();
        inv.setIdProducto(nuevo);
        inv.setCantidad(0);
        inv.setEstado("Disponible");

        InventarioJpaController invControl = new InventarioJpaController(emf);
        invControl.create(inv);

        JOptionPane.showMessageDialog(this, "Producto registrado exitosamente.");
        MenuPrincipal atras = new MenuPrincipal(usuarioActual, null, pedidoActual);
        atras.setVisible(true);
        this.dispose();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "No se pudo registrar el producto.");
    } finally {
        emf.close();
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

