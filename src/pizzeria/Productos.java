    private void txtPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioKeyTyped
        char c = evt.getKeyChar();
        String texto = txtPrecio.getText();

        // Permitir solo dígitos y un punto
        if (!Character.isDigit(c) && c != '.') {
            evt.consume();
            return;
        }

        // Evitar que el primer caracter sea un punto
        if (texto.isEmpty() && c == '.') {
            evt.consume();
            return;
        }

        // Evitar que escriban más de un punto decimal
        if (c == '.' && texto.contains(".")) {
            evt.consume();
            return;
        }

        // (Opcional) Limitar a dos decimales:
        if (texto.contains(".")) {
            int indexPunto = texto.indexOf(".");
            int decimales = texto.length() - indexPunto - 1;

            if (decimales >= 2) {  // Cambia a 3, 4, etc. si deseas más decimales
                evt.consume();
                return;
            }
        }

        // (Opcional) Limitar longitud total
        if (texto.length() >= 10) { // cambia a lo que necesites
            evt.consume();
        }

    }//GEN-LAST:event_txtPrecioKeyTyped

    private void txtNomProductoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomProductoKeyTyped
        char c = evt.getKeyChar();

        // Permite letras, números y espacio
        if (!Character.isLetterOrDigit(c) && c != KeyEvent.VK_SPACE) {
            evt.consume();  // Bloquea el carácter
        }

    }//GEN-LAST:event_txtNomProductoKeyTyped

