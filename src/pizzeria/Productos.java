    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = txtNomProducto.getText().trim();
    String categoriaSeleccionada = cbCategoria.getSelectedItem().toString();
    String ingredientesTexto = txtIngredientes.getText().trim();
    String precioTexto = txtPrecio.getText().trim();
    String cantidadTexto = txtCantidad.getText().trim();  // NUEVO CAMPO

    if (nombre.isEmpty() || ingredientesTexto.isEmpty() || precioTexto.isEmpty() || cantidadTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, rellene todos los campos.");
        return;
    }

    BigDecimal precio;
    int cantidad;

    try {
        precio = new BigDecimal(precioTexto);
        if (precio.compareTo(BigDecimal.ZERO) <= 0) {
            JOptionPane.showMessageDialog(this, "El precio debe ser mayor que 0.");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El precio no es válido.");
        return;
    }

    try {
        cantidad = Integer.parseInt(cantidadTexto);
        if (cantidad < 0) {
            JOptionPane.showMessageDialog(this, "La cantidad no puede ser negativa.");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "La cantidad debe ser un número entero.");
        return;
    }

    // Procesar ingredientes ignorando espacios → Separación solo por comas
    String[] ingredientesArray = ingredientesTexto.replace(" ", "").split(",");

    EntityManagerFactory emf = Persistence.createEntityManagerFactory("PizzeriaPU");
    EntityManager em = emf.createEntityManager();

    try {
        List<Producto> repetido = em.createQuery(
                "SELECT p FROM Producto p WHERE LOWER(p.nombre) = :n", Producto.class)
                .setParameter("n", nombre.toLowerCase())
                .getResultList();

        if (!repetido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ese producto ya existe.");
            em.close();
            emf.close();
            return;
        }
        em.getTransaction().begin();

        // 1. Crear producto
        Producto nuevo = new Producto();
        nuevo.setNombre(nombre);
        nuevo.setDescripcion(ingredientesTexto); // se guarda tal cual
        nuevo.setPrecio(precio);
        nuevo.setIdCategoria(new Categoria(cbCategoria.getSelectedIndex() + 1));

        em.persist(nuevo);
        em.flush(); // para obtener el id generado

        int idProductoGuardado = nuevo.getIdProducto();

        // 2. Registrar ingredientes en tabla ingredientes (solo si no existen)
        for (String ing : ingredientesArray) {
            if (ing.isEmpty()) continue;

            List<Ingrediente> existentes = em.createQuery(
                "SELECT i FROM Ingrediente i WHERE LOWER(i.nombre) = :n", Ingrediente.class
            ).setParameter("n", ing.toLowerCase())
             .getResultList();

            if (existentes.isEmpty()) {
                Ingrediente nuevoIng = new Ingrediente();
                nuevoIng.setNombre(ing);
                em.persist(nuevoIng);
            }
        }

        // 3. Agregar cantidad a tabla inventario
        Inventario inv = new Inventario();
        inv.setIdProducto(nuevo);       // RELACIÓN CON PRODUCTO
        inv.setCantidad(cantidad);      // CANTIDAD DEL NUEVO TEXTFIELD
        inv.setEstado(cantidad > 0 ? "Disponible" : "Agotado");

        em.persist(inv);

        em.getTransaction().commit();
        JOptionPane.showMessageDialog(this, "Producto registrado correctamente.");

        // Regresar al menú
        MenuPrincipal regreso = new MenuPrincipal(usuarioActual, null, pedidoActual);
        regreso.setVisible(true);
        this.dispose();

    } catch (Exception ex) {
        em.getTransaction().rollback();
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al registrar el producto.");
    } finally {
        em.close();
        emf.close();
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

